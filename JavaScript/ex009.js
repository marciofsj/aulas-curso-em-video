// Repetições while, do while e for

let c = 1;
while (c <= 6) {
    console.log('Olá, mundo!');
    c ++;
}

let n = 1;
while (n <= 3) {
    console.log(`Passo ${n}`);
    n ++;
}

let t = 1;
do {
    console.log(`Conte ${t}`);
    t ++;
} while (t <= 7)

for (let x=1; x<=10; x++) {
    console.log(`Enumerar ${x}`);
}
